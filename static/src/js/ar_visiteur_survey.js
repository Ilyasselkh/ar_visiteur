/** @odoo-module **/

const params = new URLSearchParams(window.location.search);
const isVisitorPopup = params.get("ar_visiteur_popup") === "1";

function normalizeText(value) {
    return (value || "")
        .normalize("NFD")
        .replace(/[\u0300-\u036f]/g, "")
        .replace(/\s+/g, " ")
        .trim()
        .toLowerCase();
}

function shouldHideBranding(element) {
    const text = normalizeText(element.textContent);
    const href = normalizeText(element.getAttribute && element.getAttribute("href"));
    return (
        text.includes("genere par") ||
        text.includes("generated by") ||
        text.includes("powered by") ||
        text.includes("odoo") && (text.includes("genere") || text.includes("generated") || text.includes("powered")) ||
        href.includes("odoo.com")
    );
}

function hideElement(element) {
    element.classList.add("ar_visiteur_hidden_branding");
    element.style.setProperty("display", "none", "important");
    element.style.setProperty("visibility", "hidden", "important");
    element.style.setProperty("opacity", "0", "important");
    element.style.setProperty("pointer-events", "none", "important");
}

function getBrandingContainer(element) {
    const explicitContainer = element.closest(
        ".o_brand_promotion, .o_website_brand_promotion, .o_powered_by_odoo, .o_footer_copyright"
    );
    if (explicitContainer) {
        return explicitContainer;
    }
    const linkOrButton = element.closest("a, button");
    if (linkOrButton) {
        return linkOrButton;
    }
    return element.closest(".card, .btn, span, small, p") || element;
}

function hideOdooBranding() {
    if (!isVisitorPopup) {
        return;
    }
    document.documentElement.classList.add("ar_visiteur_survey_popup");
    for (const selector of [
        ".o_brand_promotion",
        ".o_website_brand_promotion",
        ".o_powered_by_odoo",
        ".o_footer_copyright",
        "a[href*='odoo.com']",
        "img[src*='odoo']",
    ]) {
        for (const element of document.querySelectorAll(selector)) {
            hideElement(getBrandingContainer(element));
        }
    }
    for (const element of document.querySelectorAll("a, button, span, small, p")) {
        if (shouldHideBranding(element)) {
            hideElement(getBrandingContainer(element));
        }
    }
}

function startVisitorSurveyCleanup() {
    if (!isVisitorPopup) {
        return;
    }
    hideOdooBranding();
    const observer = new MutationObserver(() => hideOdooBranding());
    observer.observe(document.documentElement, {
        childList: true,
        subtree: true,
    });
    setTimeout(hideOdooBranding, 300);
    setTimeout(hideOdooBranding, 1000);
    setTimeout(hideOdooBranding, 2000);
}

if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", startVisitorSurveyCleanup);
} else {
    startVisitorSurveyCleanup();
}
