/** @odoo-module **/

function normalizeText(value) {
    return (value || "")
        .normalize("NFD")
        .replace(/[\u0300-\u036f]/g, "")
        .replace(/\s+/g, " ")
        .trim()
        .toLowerCase();
}

function isBrandingElement(element) {
    const text = normalizeText(element.textContent);
    const href = normalizeText(element.getAttribute && element.getAttribute("href"));
    const className = normalizeText(element.getAttribute && element.getAttribute("class"));
    return (
        text.includes("genere par") ||
        text.includes("generated by") ||
        text.includes("powered by") ||
        href.includes("odoo.com") ||
        className.includes("brand_promotion") ||
        className.includes("powered_by") ||
        className.includes("powered-by")
    );
}

function hideElement(element) {
    element.classList.add("ar_visiteur_hidden_branding");
    element.style.setProperty("display", "none", "important");
    element.style.setProperty("visibility", "hidden", "important");
    element.style.setProperty("opacity", "0", "important");
    element.style.setProperty("pointer-events", "none", "important");
}

function getBrandingContainer(element) {
    const explicitContainer = element.closest(
        ".o_brand_promotion, .o_website_brand_promotion, .o_powered_by_odoo, .o_footer_copyright"
    );
    if (explicitContainer) {
        return explicitContainer;
    }
    const linkOrButton = element.closest("a, button");
    if (linkOrButton) {
        return linkOrButton;
    }
    return element.closest(".card, .btn, span, small, p") || element;
}

function injectVisitorSurveyStyle(doc) {
    if (!doc || doc.getElementById("ar_visiteur_iframe_style")) {
        return;
    }
    const style = doc.createElement("style");
    style.id = "ar_visiteur_iframe_style";
    style.textContent = `
        html, body, #wrapwrap, main {
            overflow-x: hidden !important;
            background: #f8fbfb !important;
        }
        .ar_visiteur_hidden_branding,
        .o_brand_promotion,
        .o_website_brand_promotion,
        .o_powered_by_odoo,
        .o_footer_copyright,
        a[href*="odoo.com"],
        img[src*="odoo"],
        [class*="brand_promotion"],
        [class*="powered_by"],
        [class*="powered-by"] {
            display: none !important;
            visibility: hidden !important;
            opacity: 0 !important;
            pointer-events: none !important;
        }
        .btn-primary,
        button.btn-primary,
        a.btn-primary {
            border-color: #0f766e !important;
            border-radius: 8px !important;
            background: #0f766e !important;
            color: #fff !important;
            font-weight: 800 !important;
            box-shadow: 0 12px 26px rgba(15, 118, 110, 0.2) !important;
        }
        .btn-primary:hover,
        button.btn-primary:hover,
        a.btn-primary:hover {
            border-color: #115e59 !important;
            background: #115e59 !important;
        }
    `;
    doc.head.appendChild(style);
}

function cleanVisitorSurveyIframe(iframe) {
    let doc;
    try {
        doc = iframe.contentDocument || iframe.contentWindow.document;
    } catch {
        return;
    }
    if (!doc) {
        return;
    }
    injectVisitorSurveyStyle(doc);
    const selectors = [
        ".o_brand_promotion",
        ".o_website_brand_promotion",
        ".o_powered_by_odoo",
        ".o_footer_copyright",
        "a[href*='odoo.com']",
        "img[src*='odoo']",
        "[class*='brand_promotion']",
        "[class*='powered_by']",
        "[class*='powered-by']",
    ];
    for (const selector of selectors) {
        for (const element of doc.querySelectorAll(selector)) {
            hideElement(getBrandingContainer(element));
        }
    }
    for (const element of doc.querySelectorAll("a, button, span, small, p")) {
        if (isBrandingElement(element)) {
            hideElement(getBrandingContainer(element));
        }
    }
}

function watchVisitorSurveyIframe(iframe) {
    if (iframe.dataset.arVisitorWatched === "1") {
        return;
    }
    iframe.dataset.arVisitorWatched = "1";
    const run = () => {
        cleanVisitorSurveyIframe(iframe);
        try {
            const doc = iframe.contentDocument || iframe.contentWindow.document;
            if (doc && !doc.documentElement.dataset.arVisitorObserver) {
                doc.documentElement.dataset.arVisitorObserver = "1";
                new MutationObserver(() => cleanVisitorSurveyIframe(iframe)).observe(doc.documentElement, {
                    childList: true,
                    subtree: true,
                });
            }
        } catch {
            return;
        }
    };
    iframe.addEventListener("load", run);
    setTimeout(run, 300);
    setTimeout(run, 1000);
    setTimeout(run, 2000);
}

function watchQuizPopups() {
    for (const iframe of document.querySelectorAll("iframe.ar_visiteur_quiz_iframe")) {
        watchVisitorSurveyIframe(iframe);
    }
}

watchQuizPopups();
new MutationObserver(watchQuizPopups).observe(document.documentElement, {
    childList: true,
    subtree: true,
});
